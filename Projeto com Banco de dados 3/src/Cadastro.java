    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        try {
            
            Connection conexao = new Conexao ().conectar() ;
            
            if (conexao != null) {
                PreparedStatement ps;
                String sql = "insert into login(nome,cpf,telefone,endereco,senha)values(?,?,?,?,?)";     //gravando na tabela produto, qtd de campos e quantidade de "?" iguais
                ps = conexao.prepareStatement(sql);
                
                ps.setString (1, txtNome.getText() );
                ps.setString (2, txtCpf.getText() );
                ps.setString (3, txtTelefone.getText() );
                ps.setString (4, txtEndereco.getText() );
                ps.setString (5, jPasswordField1.getPassword().toString() );
                
                ps.executeUpdate();
                conexao.close();
                new Usuario().setVisible(true);
            }
        } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Erro nas parada: " + e.getMessage() );
            }
    }//GEN-LAST:event_jButton2ActionPerformed

