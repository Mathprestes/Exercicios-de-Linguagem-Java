    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        try {
            Connection conexao = new Conexao ().conectar() ;
            if (conexao != null) {
                PreparedStatement ps;
                String sql = "insert into produto(descricao,valor)values(?,?)";     //gravando na tabela produto, qtd de campos e quantidade de "?" iguais
                ps = conexao.prepareStatement(sql);
                
                ps.setString (1, txtDescricao.getText() );
                ps.setFloat  (2, Float.parseFloat(txtValor.getText()) );
                
                ps.executeUpdate();
                conexao.close();
                JOptionPane.showMessageDialog(null, "Gravado com sucesso");
            }
        } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Erro nas parada: " + e.getMessage() );
            }
    }//GEN-LAST:event_jButton1ActionPerformed

